/*Write a program to make Simple calculator (to make addition,
subtraction,multiplication, division and modulo)*/
#include<stdio.h>
main()
{
	int a=30,b=10;
	printf("\n\n\t addition           :%d+%d   =  %d",a,b,a+b);
	printf("\n\n\t subtraction        :%d-%d   =  %d",a,b,a-b);
	printf("\n\n\t multiplication     :%d*%d   =  %d",a,b,a*b);
	printf("\n\n\t division           :%d/%d   =  %d",a,b,a/b);
	printf("\n\n\t modulo             :%dmod%d =  %d",a,b,a%b);
	
} 
